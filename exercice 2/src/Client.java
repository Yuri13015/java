public class Client {
    public String nom;
    public String prenom;

    public String adresse;
    public Integer codePostal;
    public String ville;


    public Client(String nom, String prenom, String adresse, Integer codePostal, String ville) {
        this.nom = nom;
        this.prenom = prenom;
        this.adresse = adresse;
        this.codePostal = codePostal;
        this.ville = ville;
    }
}
